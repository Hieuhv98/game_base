    }

    public Sprite GetFlag(string countryCode) 
    {
        Sprite flag = null;
        foreach(var country in countries) 
        {
            if (country.countryCode.ToString().Equals(countryCode)) 
            {
                flag =  country.flag;
                break;
            }
        }
        return flag;
    }

    public int GetCountryIndexDefaut(string countryCode) 
    {
        int index = 0;
        foreach(var country in countries) 
        {
            if (country.countryCode.ToString().Equals(countryCode)) 
            {
                break;
            }
            index++;
        }

        //GameData.IndexCountryCurrent = index;
        return index;
    }

