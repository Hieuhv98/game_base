    public void Execute()
    {
        string[] guids2 = AssetDatabase.FindAssets("t:texture2D", new[] { "Assets\\FishPin\\Sprites\\CountryIcon" });
        for (int i = 0; i < 230; i++)
        {
            countries[i].countryCode = (ECountryCode)i;
            countries[i].countryName = countryName[i];

            for (int j = 0; j < guids2.Length; j++)
            {
                var path = AssetDatabase.GUIDToAssetPath(guids2[j]);
                if (countries[i].countryCode.ToString() == Path.GetFileNameWithoutExtension(path))
                {
                    countries[i].flag = AssetDatabase.LoadAssetAtPath<Sprite>(path);
                }
            }
        }

